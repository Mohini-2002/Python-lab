#Endswith() and its class
ST1 = input("Enter a string :")
ST1 = ST1.endswith("ing")
print(ST1, type(ST1))

#Find() and its class or type
ST2 = input("Enter a string :")
ST2 = ST2.find("ING")
print(ST2, type(ST2))

#Isalnum() and its type
ST3 = input("Enter a string :")
ST3 = ST3.isalnum()
print(ST3, type(ST3))

#Isalpha() and its type
ST4 = input('Enter a string :')
ST4 = ST4.isalpha()
print(ST4, type(ST4))

#Isdigit() and its type
ST5 = input("Enter a string:")
ST5 = ST5.isdigit()
print(ST5, type(ST5))